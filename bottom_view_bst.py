class Solution:
    def bottomView(self, root):
        # code here
        queue = [[root, 0]]
        result = {}
        while queue:
            node = queue.pop(0)
            cur = node[0]
            val = node[0].data
            col = node[1]
            # unlike top view we dont need to check whether the key is already present or not
            result[col] = val
            if cur.left:
                queue.append([cur.left, col-1])
            if cur.right:
                queue.append([cur.right, col+1])
        # output in the ordered fashion(key)
        key = []
        for i in result.keys():
            key.append(i)
        key.sort()
        finals = []
        for i in key:
            finals.append(result[i])
        return finals
